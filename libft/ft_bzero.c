#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*p;

	i = 0;
	p = (unsigned char *)s;
	while (0 < n)
	{
		p[i] = 0;
		n--;
		i++;
	}
}
